import numpy as np

arr = np.array([1, 4, 9, 16])
print(np.sqrt(arr))  # Element-wise square root # Element-wise square root
print(np.exp(arr))    # Element-wise exponential
print(np.log(arr))    # Element-wise natural logarithm
print(np.sin(arr))    # Element-wise sine
print(np.cos(arr))    # Element-wise cosine
print(np.tan(arr))    # Element-wise tangent